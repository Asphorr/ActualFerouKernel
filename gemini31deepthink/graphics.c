void init_graphics() {
    uint8_t* mode_info = (uint8_t*)0x5000;
    vbe_pitch = *(uint16_t*)(mode_info + 0x10); 
    screen_w = *(uint16_t*)(mode_info + 0x12);
    screen_h = *(uint16_t*)(mode_info + 0x14); 
    vbe_bpp = *(uint8_t*)(mode_info + 0x19);
    LFB = (uint8_t*)(*(uint32_t*)(mode_info + 0x28));
    if (screen_w == 0) { screen_w = 1024; }
    if (screen_h == 0) { screen_h = 768; }
    back_buffer = (uint8_t*)kmalloc(vbe_pitch * screen_h);
    draw_rect(0, 0, screen_w, screen_h, 0x000000);
}

void put_pixel_buf(int x, int y, uint32_t color) {
    if (x >= 0 && x < screen_w && y >= 0 && y < screen_h) {
        uint32_t offset = y * vbe_pitch + x * (vbe_bpp / 8);
        if (vbe_bpp == 32) { *(uint32_t*)(back_buffer + offset) = color; } 
        else if (vbe_bpp == 24) { back_buffer[offset] = color & 0xFF; back_buffer[offset + 1] = (color >> 8) & 0xFF; back_buffer[offset + 2] = (color >> 16) & 0xFF; }
    }
}

void draw_alpha_rect(int x, int y, int w, int h, uint32_t color, uint8_t alpha) {
    if (x < 0) { w += x; x = 0; } if (y < 0) { h += y; y = 0; }
    if (x + w > screen_w) { w = screen_w - x; } if (y + h > screen_h) { h = screen_h - y; }
    if (w <= 0 || h <= 0) { return; }
    int bpp = vbe_bpp / 8; uint32_t sr = (color >> 16) & 0xFF, sg = (color >> 8) & 0xFF, sb = color & 0xFF;
    for (int i = 0; i < h; i++) {
        uint32_t offset = (y + i) * vbe_pitch + x * bpp;
        if (vbe_bpp == 32) { 
            uint32_t* row = (uint32_t*)(back_buffer + offset); 
            for (int j = 0; j < w; j++) {
                uint32_t bg = row[j]; uint32_t br = (bg>>16)&0xFF, bg_g = (bg>>8)&0xFF, bb = bg&0xFF;
                row[j] = (((sr * alpha + br * (255 - alpha)) / 255) << 16) | (((sg * alpha + bg_g * (255 - alpha)) / 255) << 8) | ((sb * alpha + bb * (255 - alpha)) / 255);
            } 
        } else if (vbe_bpp == 24) { 
            uint8_t* ptr = back_buffer + offset; 
            for (int j = 0; j < w; j++) {
                uint32_t bb = ptr[0], bg_g = ptr[1], br = ptr[2];
                ptr[0] = (sb * alpha + bb * (255 - alpha)) / 255; ptr[1] = (sg * alpha + bg_g * (255 - alpha)) / 255; ptr[2] = (sr * alpha + br * (255 - alpha)) / 255; ptr += 3;
            } 
        }
    }
}

